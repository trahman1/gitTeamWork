package pasta;

public class PastaDetails {
    int pastaPrice;
    String pastaName;
    String pastaType;

    public PastaDetails(int pastaPrice) {
        this.pastaPrice = pastaPrice;
    }

    public PastaDetails(int pastaPrice, String pastaName) {
        this.pastaPrice = pastaPrice;
        this.pastaName = pastaName;
    }

    public PastaDetails(int pastaPrice, String pastaName, String pastaType) {
        this.pastaPrice = pastaPrice;
        this.pastaName = pastaName;
        this.pastaType = pastaType;
    }












}








